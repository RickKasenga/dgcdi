                            spatial PRIDS, les IDS utiliser comme l’un des instruments de stratégie
                             de développement et d’intégration économique de l’Afrique et de l’Union 
                             africaine à travers l’une des structures techniques nommé NEPAD. Le concept 
                             des initiatives de développement spatial IDS en sigle dit aussi corridors de 
                             développement, née en 1996 sous l’impulsion des ministères sud-africain de transport, 
                             du commerce et d’industrie pour répondre aux besoins politique macro-économique du pays 
                             après la ségrégation. Mais également utilisé comme un outil de politique économique de
                              l’Afrique du Sud dans le cadre de sa coopération bilatérale et multilatérale avec les
                               autres pays africains plus particulièrement dans la zone SADC.
                            La république Démocratique du Congo a intégré les IDS à travers sa coopération bilatérale 
                            avec l’Afrique du Sud, berceau du concept, par le projet pilote « Corridor de Développement
                             Bas Congo », BCDC en sigle, en impliquant 3 pays : la République Démocratique du Congo, 
                             la République du Congo et la République d’Angola, il est une démarche de promotion et de
                              réalisation des investissements multisectoriels publics et ou privés. 
                            L’importance de la République Démocratique du Congo dans les IDS a conduit à
                            la création d’une direction générale des corridors de développement industriel avec comme ambition de coordonner tous les corridors transfrontaliers de la RDC avec les pays concernés par ces corridors.

