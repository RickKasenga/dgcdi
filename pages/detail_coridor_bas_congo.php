                                    <h3>Corridor de Développement Bas Congo</h3>
                                    <p class="text-justify">
                                    
                                    RDC et RSA liées par un Accord Cadre de Coopération depuis janvier 2004 a ce jour 37 accords sectoriels dont celui de la Coopération Economique (avril 2004). 
                                    La coopération RDC – RSA permet à cette première l’accès aux financements BRICS (Brésil, Russie, Inde, Chine, South Africa). <br><br>

                                    Sous l’accord de la coopération économique signé le 30 avril 2004, CDBC en sigle (2009), le projet pilote ‘’ Corridor de développement Bas-Congo ‘’ fruit de la coopération bilatérale RDC, 
                                    le projet est à vocation de développement et d’intégration régionale impliquant trois pays :
                                    <ul>
                                        <li>RDC, (La ville province de Kinshasa, les provinces du Kongo Central et Kwango);</li>
                                        <li>Congo Brazza (l’axe Brazzaville -Pointe-Noire prenant en compte la partie sud du pays);</li>
                                        <li>Angola (province de lunda norte et l’enclave de kabinda).</li>
                                    </ul>

                                    </p>
                                    
                                    <blockquote>
                                    Direction Générale des Corridors de Développement Industriel créée en mars 2020 pour gérer les 10 corridors liant la RDC à dix autres pays de la SADC, la COMESA, la CEEAC, la CPGL et la CPAE 
                                    et en créer d’autres en fonction des impératifs économiques. Une particularité congolaise.
                                    </blockquote>
                                    <p>
                                    Stratégies développées dans le Corridor de Développement Bas Congo <br>
